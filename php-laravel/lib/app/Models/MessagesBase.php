<?php
/**
 * MessagesBase
 */
namespace app\Models;

/**
 * MessagesBase
 */
class MessagesBase {

    /** @var string $avatarUrl The URL of the user&#39;s avatar.  Can be null only if client_gravatar was passed, which means that the user has not uploaded an avatar in Zulip, and the client should compute the gravatar URL by hashing the user&#39;s email address itself for this user.*/
    private $avatarUrl;

    /** @var string $client A Zulip \&quot;client\&quot; string, describing what Zulip client sent the message.*/
    private $client;

    /** @var string $content The content/body of the message.*/
    private $content;

    /** @var string $contentType The HTTP &#x60;content_type&#x60; for the message content.  This will be &#x60;text/html&#x60; or &#x60;text/x-markdown&#x60;, depending on whether &#x60;apply_markdown&#x60; was set.*/
    private $contentType;

    /** @var OneOfStringArray $displayRecipient Data on the recipient of the message; either the name of a stream or a dictionary containing basic data on the users who received the message.*/
    private $displayRecipient;

    /** @var int $id The unique message ID.  Messages should always be displayed sorted by ID.*/
    private $id;

    /** @var bool $isMeMessage Whether the message is a [/me status message][status-messages]  [status-messages]: /help/format-your-message-using-markdown#status-messages*/
    private $isMeMessage;

    /** @var \app\Models\EmojiReaction[] $reactions Data on any reactions to the message.*/
    private $reactions;

    /** @var int $recipientId A unique ID for the set of users receiving the message (either a stream or group of users).  Useful primarily for hashing.*/
    private $recipientId;

    /** @var string $senderEmail The Zulip display email address of the message&#39;s sender.*/
    private $senderEmail;

    /** @var string $senderFullName The full name of the message&#39;s sender.*/
    private $senderFullName;

    /** @var int $senderId The user ID of the message&#39;s sender.*/
    private $senderId;

    /** @var string $senderRealmStr A string identifier for the realm the sender is in.  Unique only within the context of a given Zulip server.  E.g. on &#x60;example.zulip.com&#x60;, this will be &#x60;example&#x60;.*/
    private $senderRealmStr;

    /** @var int $streamId Only present for stream messages; the ID of the stream.*/
    private $streamId;

    /** @var string $subject The &#x60;topic&#x60; of the message.  Currently always &#x60;\&quot;\&quot;&#x60; for private messages, though this could change if Zulip adds support for topics in private message conversations.  The field name is a legacy holdover from when topics were called \&quot;subjects\&quot; and will eventually change.*/
    private $subject;

    /** @var \app\Models\MessagesBaseTopicLinks[] $topicLinks Data on any links to be included in the &#x60;topic&#x60; line (these are generated by [custom linkification filters](/help/add-a-custom-linkifier) that match content in the message&#39;s topic.)  **Changes**: This field contained a list of urls before   Zulip 4.0 (feature level 46).  New in Zulip 3.0 (feature level 1): Previously, this field was called &#x60;subject_links&#x60;; clients are recommended to rename &#x60;subject_links&#x60; to &#x60;topic_links&#x60; if present for compatibility with older Zulip servers.*/
    private $topicLinks;

    /** @var string[] $submessages Data used for certain experimental Zulip integrations.*/
    private $submessages;

    /** @var int $timestamp The UNIX timestamp for when the message was sent, in UTC seconds.*/
    private $timestamp;

    /** @var string $type The type of the message: &#x60;stream&#x60; or &#x60;private&#x60;.*/
    private $type;

}
